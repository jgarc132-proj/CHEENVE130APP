package com.example.chen_enen130app.Lesson_RVA;

public class ModelClass {
    int image;
    String text;

    public ModelClass(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
